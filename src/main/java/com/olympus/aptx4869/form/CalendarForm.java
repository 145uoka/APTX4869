package com.olympus.aptx4869.form;

public class CalendarForm{

    private String income;

    private String remainingBudget;

    private String spending;

    private String regularlyIncome;

    private String regularlySpending;

    private String balance;

    private String estimate;

    private String moneyReceptionDate;

    private String userId;

    private String lineId;

    public String getIncome() {
        return income;
    }

    public void setIncome(String income) {
        this.income = income;
    }

    public String getRemainingBudget() {
        return remainingBudget;
    }

    public void setRemainingBudget(String remainingBudget) {
        this.remainingBudget = remainingBudget;
    }

    public String getSpending() {
        return spending;
    }

    public void setSpending(String spending) {
        this.spending = spending;
    }

    public String getRegularlyIncome() {
        return regularlyIncome;
    }

    public void setRegularlyIncome(String regularlyIncome) {
        this.regularlyIncome = regularlyIncome;
    }

    public String getRegularlySpending() {
        return regularlySpending;
    }

    public void setRegularlySpending(String regularlySpending) {
        this.regularlySpending = regularlySpending;
    }

    public String getBalance() {
        return balance;
    }

    public void setBalance(String balance) {
        this.balance = balance;
    }

    public String getEstimate() {
        return estimate;
    }

    public void setEstimate(String estimate) {
        this.estimate = estimate;
    }

    public String getMoneyReceptionDate() {
        return moneyReceptionDate;
    }

    public void setMoneyReceptionDate(String moneyReceptionDate) {
        this.moneyReceptionDate = moneyReceptionDate;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getLineId() {
        return lineId;
    }

    public void setLineId(String lineId) {
        this.lineId = lineId;
    }

}